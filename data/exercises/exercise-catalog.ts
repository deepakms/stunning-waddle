// ============================================
// EXERCISE LOOKUP FUNCTIONS
// ============================================

export function getExerciseById(id: string): ExerciseDefinition | undefined {
  return ALL_EXERCISES.find(e => e.id === id);
}

export function getExercisesByMuscleGroup(muscleGroup: MuscleGroup): ExerciseDefinition[] {
  return ALL_EXERCISES.filter(e => e.muscle_group === muscleGroup);
}

export function getExercisesByDifficulty(difficulty: number): ExerciseDefinition[] {
  return ALL_EXERCISES.filter(e => e.difficulty === difficulty);
}

export function getExercisesByCategory(category: ExerciseCategory): ExerciseDefinition[] {
  return ALL_EXERCISES.filter(e => e.category === category);
}

export function getExercisesWithoutEquipment(): ExerciseDefinition[] {
  return ALL_EXERCISES.filter(e =>
    e.equipment_required.length === 1 && e.equipment_required[0] === 'none'
  );
}

export function getExercisesSafeForInjury(injuries: InjuryType[]): ExerciseDefinition[] {
  return ALL_EXERCISES.filter(e =>
    !injuries.some(injury => e.contraindicated_injuries.includes(injury))
  );
}
