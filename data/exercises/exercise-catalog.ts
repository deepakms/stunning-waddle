// ============================================
// COMPLETE EXERCISE DEFINITION
// ============================================

export interface ExerciseDefinition {
  id: string;
  name: string;
  description: string;
  instructions: string[];

  // Classification
  muscle_group: MuscleGroup;
  secondary_muscles: MuscleGroup[];
  exercise_type: ExerciseType;
  movement_pattern: MovementPattern;
  category: ExerciseCategory;

  // Difficulty (1-5 scale)
  difficulty: number;
  difficulty_label: DifficultyLabel;

  // Difficulty factors (each 0-1 scale, sum affects overall difficulty)
  difficulty_factors: {
    strength_required: number; // Raw strength needed
    coordination: number; // Motor skill complexity
    balance: number; // Stability required
    flexibility: number; // ROM required
    endurance: number; // Stamina needed
    impact: number; // Joint stress level
  };

  // Timing
  default_duration_seconds: number | null;
  default_reps: number | null;
  is_timed: boolean;
  rest_between_sets_seconds: number;

  // Intensity and energy
  intensity_level: IntensityLevel;
  tempo: ExerciseTempo;
  energy_system: EnergySystem;

  // Physical requirements
  body_position: BodyPosition;
  space_required: SpaceRequired;
  equipment_required: EquipmentType[];
  equipment_alternatives: EquipmentType[][];

  // Partner exercise properties
  is_partner_exercise: boolean;
  requires_contact: boolean;
  contact_level: ContactLevel;
  partner_sync_required: boolean; // Both must move together

  // Injury contraindications
  contraindicated_injuries: InjuryType[];
  caution_injuries: InjuryType[]; // Can do with modification

  // Scaling options
  easier_variation: string | null;
  harder_variation: string | null;
  scaling_notes: string;

  // Fitness assessment correlations
  requires_pushup_ability: boolean;
  requires_plank_ability: boolean;
  requires_squat_ability: boolean;
  minimum_cardio_capacity: 'under_5min' | '5_15min' | '15_30min' | 'over_30min' | null;

  // Calories and metrics
  calories_per_minute_low: number;
  calories_per_minute_high: number;
  met_value: number;

  // Tags for filtering
  tags: string[];

  // Media
  video_url: string | null;
  thumbnail_url: string | null;
}

// ============================================
// EXERCISE PAIRING RULES
// ============================================

export interface ExercisePairDefinition {
  id: string;
  exercise_a_id: string; // Easier/lower intensity
  exercise_b_id: string; // Harder/higher intensity
  muscle_group: MuscleGroup;
  duration_seconds: number;

  // Difficulty characteristics
  difficulty_a: number;
  difficulty_b: number;
  difficulty_gap: number;

  // Compatibility scores (0-1)
  compatibility: {
    movement_similarity: number; // How similar the movements are
    equipment_compatibility: number; // Can use same/similar equipment
    space_compatibility: number; // Both can work in same space
    intensity_balance: number; // Appropriate intensity gap
  };

  // When to use this pair
  suitable_for_goals: string[];
  suitable_for_experience_gap: 'small' | 'medium' | 'large';

  // Tags
  tags: string[];
}

